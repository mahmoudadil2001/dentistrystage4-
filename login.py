def find_username_by_fullname_and_phone(full_name, phone):
    try:
        res = requests.post(GOOGLE_SCRIPT_URL, data={
            "action": "find_username",
            "full_name": full_name,
            "phone": phone
        }, timeout=120)
        username = res.text.strip()
        if username and username != "NOT_FOUND":
            return username
        return None
    except:
        return None

